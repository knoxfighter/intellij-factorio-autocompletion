package moe.knox.factorio.parser.apiData;

/**
 * Definition of global objects (just variables in global scope)
 */
public class GlobalObject {
    /**
     * The name of the member.
     */
    public String name;

    /**
     * The order of the member as shown in the html.
     */
    public double order;

    /**
     * The text description of the member.
     */
    public String description;

    /**
     * The class name of the global object.
     */
    public String type;
}
