package moe.knox.factorio.api.parser.data;

/**
 *  The representation of Builtin Types
 *
 *  These have to be hardcoded since there is no information what they really are :(
 */
public class BuiltinType {
    /**
     * The name of the builtin type.
     */
    String name;

    /**
     * The order of the builtin type as shown in the html.
     */
    double order;

    /**
     * The text description of the builtin type.
     */
    String description;
}
